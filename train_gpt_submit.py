def quantize_dequantize_by_mode(t: Tensor, mode: str) -> Tensor:
    if mode == "fp16":
        return t
    if mode == "int8":
        return quantize_dequantize_int8(t)
    if mode == "int6":
        return quantize_dequantize_int6(t)
    raise ValueError(f"Unsupported quantization mode: {mode!r}")

