def quantize_float_tensor_int6(t: Tensor) -> tuple[Tensor, Tensor]:
    t32 = t.float()
    if t32.ndim == 2:
        # Matrices get one scale per row, which usually tracks output-channel
        # ranges much better than a single tensor-wide scale.
        clip_abs = (
            torch.quantile(t32.abs(), INT6_CLIP_Q, dim=1)
            if t32.numel()
            else torch.empty((t32.shape[0],), dtype=torch.float32)
        )
        clipped = torch.maximum(torch.minimum(t32, clip_abs[:, None]), -clip_abs[:, None])
        scale = (clip_abs / INT6_MAX_Q).clamp_min(1.0 / INT6_MAX_Q)
        q = torch.clamp(torch.round(clipped / scale[:, None]), -INT6_MAX_Q, INT6_MAX_Q).to(torch.int8).contiguous()
        return q, scale.to(dtype=INT6_PER_ROW_SCALE_DTYPE).contiguous()

    # Vectors / scalars use a simpler per-tensor scale.
    clip_abs = float(torch.quantile(t32.abs().flatten(), INT6_CLIP_Q).item()) if t32.numel() else 0.0
    scale = torch.tensor(clip_abs / INT6_MAX_Q if clip_abs > 0 else 1.0, dtype=torch.float32)
    q = torch.clamp(torch.round(torch.clamp(t32, -clip_abs, clip_abs) / scale), -INT6_MAX_Q, INT6_MAX_Q).to(torch.int8).contiguous()
    return q, scale


def quantize_float_tensor_int6_per_col(t: Tensor) -> tuple[Tensor, Tensor]:
    t32 = t.float()
    if t32.ndim == 2:
        clip_abs = torch.quantile(t32.abs(), INT6_CLIP_Q, dim=0).clamp_min(1e-12)
        q = torch.clamp(torch.round(torch.clamp(t32, -clip_abs[None, :], clip_abs[None, :]) / clip_abs[None, :] * INT6_MAX_Q), -INT6_MAX_Q, INT6_MAX_Q)
        return q.to(torch.int8).contiguous(), clip_abs.to(dtype=INT6_PER_ROW_SCALE_DTYPE).contiguous()
    return quantize_float_tensor_int6(t)

def quantize_float_tensor_int8(t: Tensor) -> tuple[Tensor, Tensor]:
    t32 = t.float()
    if t32.ndim == 2:
        # Matrices get one scale per row, which usually tracks output-channel
        # ranges much better than a single tensor-wide scale.
        clip_abs = (
            torch.quantile(t32.abs(), INT8_CLIP_Q, dim=1)
            if t32.numel()
            else torch.empty((t32.shape[0],), dtype=torch.float32)
        )
        clipped = torch.maximum(torch.minimum(t32, clip_abs[:, None]), -clip_abs[:, None])
        scale = (clip_abs / INT8_MAX_Q).clamp_min(1.0 / INT8_MAX_Q)
        q = torch.clamp(torch.round(clipped / scale[:, None]), -INT8_MAX_Q, INT8_MAX_Q).to(torch.int8).contiguous()
        return q, scale.to(dtype=INT8_PER_ROW_SCALE_DTYPE).contiguous()

    # Vectors / scalars use a simpler per-tensor scale.
    clip_abs = float(torch.quantile(t32.abs().flatten(), INT8_CLIP_Q).item()) if t32.numel() else 0.0
    scale = torch.tensor(clip_abs / INT8_MAX_Q if clip_abs > 0 else 1.0, dtype=torch.float32)
    q = torch.clamp(torch.round(torch.clamp(t32, -clip_abs, clip_abs) / scale), -INT8_MAX_Q, INT8_MAX_Q).to(torch.int8).contiguous()
    return q, scale


def quantize_float_tensor_int8_per_col(t: Tensor) -> tuple[Tensor, Tensor]:
    t32 = t.float()
    if t32.ndim == 2:
        clip_abs = torch.quantile(t32.abs(), INT8_CLIP_Q, dim=0).clamp_min(1e-12)
        q = torch.clamp(torch.round(torch.clamp(t32, -clip_abs[None, :], clip_abs[None, :]) / clip_abs[None, :] * INT8_MAX_Q), -INT8_MAX_Q, INT8_MAX_Q)
        return q.to(torch.int8).contiguous(), clip_abs.to(dtype=INT8_PER_ROW_SCALE_DTYPE).contiguous()
    return quantize_float_tensor_int8(t)

def quantize_float_tensor_by_mode(t: Tensor, mode: str) -> tuple[Tensor, Tensor]:
    if mode == "int6":
        return quantize_float_tensor_int6_per_col(t)
    if mode == "int8":
        return quantize_float_tensor_int8_per_col(t)
    raise ValueError(f"Unsupported quantized mode {mode!r}")

DTYPE_TO_CODE = {torch.float16: 0, torch.bfloat16: 1, torch.float32: 2, torch.int64: 3, torch.int32: 4, torch.uint8: 5}
CODE_TO_DTYPE = {v: k for k, v in DTYPE_TO_CODE.items()}


QUANT_MODE_TO_CODE = {"int6": 0, "int8": 1, "fp16": 2}
CODE_TO_QUANT_MODE = {v: k for k, v in QUANT_MODE_TO_CODE.items()}


def quantize_state_dict_v(state_dict: dict[str, Tensor]):
    # Compact export object:
    # {"fmt":"v","q":{...},"s":{...},"p":{...},"d":{name:dtype_code}}
    quantized: dict[str, Tensor] = {}
    scales: dict[str, Tensor] = {}
    passthrough: dict[str, Tensor] = {}
    dtypes: dict[str, int] = {}
    quant_modes: dict[str, int] = {}
    stats = dict.fromkeys(
        ("param_count", "num_tensors", "num_float_tensors", "num_nonfloat_tensors", "baseline_tensor_bytes", "qat_payload_bytes"),
        0,
    )

    for name, tensor in state_dict.items():
        t = tensor.detach().to("cpu").contiguous()
        stats["param_count"] += int(t.numel())
        stats["num_tensors"] += 1
        stats["baseline_tensor_bytes"] += tensor_nbytes(t)

        dtypes[name] = DTYPE_TO_CODE.get(t.dtype, DTYPE_TO_CODE[torch.float16])
        quant_mode = resolve_quant_mode(name)
        quant_modes[name] = QUANT_MODE_TO_CODE[quant_mode]
        if not t.is_floating_point():
            stats["num_nonfloat_tensors"] += 1
            passthrough[name] = t
            stats["qat_payload_bytes"] += tensor_nbytes(t)
            continue

        if quant_mode == "fp16" or t.numel() <= INT6_KEEP_FLOAT_MAX_NUMEL:
            kept = t.to(dtype=INT6_KEEP_FLOAT_STORE_DTYPE).contiguous()
            passthrough[name] = kept
            dtypes[name] = DTYPE_TO_CODE.get(t.dtype, DTYPE_TO_CODE[torch.float16])
            stats["qat_payload_bytes"] += tensor_nbytes(kept)
            continue

        stats["num_float_tensors"] += 1
        q, s = quantize_float_tensor_by_mode(t, quant_mode)
        quantized[name] = q
        scales[name] = s
        stats["qat_payload_bytes"] += tensor_nbytes(q) + tensor_nbytes(s)
    return {"fmt": "v", "q": quantized, "s": scales, "p": passthrough, "d": dtypes, "m": quant_modes}, stats


def dequantize_state_dict_v(obj: dict[str, object]) -> dict[str, Tensor]:
    out: dict[str, Tensor] = {}
    quant_modes = obj.get("m", {})
    for name, q in obj["q"].items():
        dtype = CODE_TO_DTYPE[int(obj["d"][name])]
        mode = CODE_TO_QUANT_MODE.get(int(quant_modes.get(name, QUANT_MODE_TO_CODE["int6"])), "int6")
        max_q = INT8_MAX_Q if mode == "int8" else INT6_MAX_Q
        s = obj["s"][name].to(dtype=torch.float32)
        if s.ndim > 0 and q.ndim == 2:
            out[name] = (q.float() * (s.view(1, s.shape[0]) / max_q)).to(dtype=dtype).contiguous()
        else:
            out[name] = (q.float() * float(s.item())).to(dtype=dtype).contiguous()
    for name, t in obj["p"].items():
        out[name] = t.detach().to("cpu").to(dtype=CODE_TO_DTYPE[int(obj["d"][name])]).contiguous()
    return out


def compress_blob(raw: bytes, codec: str, level: int) -> bytes:
    if codec == "zstd":
        if zstd is None:
            raise RuntimeError("EXPORT_CODEC=zstd requested but zstandard is not installed")
        return zstd.ZstdCompressor(level=level).compress(raw)
    if codec == "zlib":
        return zlib.compress(raw, level=max(1, min(level, 9)))
    raise ValueError(f"Unknown codec: {codec}")


def decompress_blob(blob: bytes, codec: str) -> bytes:
    if codec == "zstd":
        if zstd is None:
            raise RuntimeError("EXPORT_CODEC=zstd requested but zstandard is not installed")
        return zstd.ZstdDecompressor().decompress(blob)
    if codec == "zlib":
        return zlib.decompress(blob)
    raise ValueError(f"Unknown codec: {codec}")


